public class Digital_watch {
    public int hours,min,sec;
    public Digital_watch(int hours, int min, int sec){
        this.hours = hours;
        this.min = min;
        this.sec = sec;
    }
    public int getHour(){
        return this.hours;
    }
    public int getMin(){
        return this.min;
    }
    public int getSec(){
        return this.sec;
    }
}
